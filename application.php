System Operation
The nacelle and the wheels are articulated on longitudinal axes allowing tilting when you turn and correction of the angle on slopes.

To produce the pivot in the right direction (towards the inside of the turn or uphill), it is necessary that:

The pivoting axis of the platform is located above the center of gravity.
Pivot axis of each wheel passes under the contact point of the wheel with the ground.
The nacelle on one side and the wheels on the other side, benefits independently to each other, from a pendular equilibrium and could theoretically not be associated in their pivoting movement.

"The concept of pendulum tilt is protected by international patents"

Pendulum principle
The word pendulum is often wrongly used to describe to a tilting vehicle.

A "Pendulum based Vehicle" is a vehicle that tilt positions in balance when it applies the force of gravity, the centrifugal force and the ground reaction. There is therefore no activator or electronic management.

The operation is passive and not active so it is lively and reliable. Vehicles with an active tilt activator all faced the problem of the response speed of the activator within acceptable power consumption. In an emergency situation a delay in movement of the actuator could be dangerous.
